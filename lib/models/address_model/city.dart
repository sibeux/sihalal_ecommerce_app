class City {
  final String idCity, name, area, type;
  final String idProvince, province, postalCode;
  final bool isFistLetter;

  City({
    required this.idCity,
    required this.name,
    required this.area,
    required this.type,
    required this.idProvince,
    required this.province,
    required this.postalCode,
    required this.isFistLetter,
  });
}
